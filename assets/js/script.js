function showRepos(data, indice) {
  const features = document.querySelector(".features");
  let link = `https://${data.owner.login}.github.io/${data.name}/`;
  let stars = data.stargazers_count;
  const article = document.createElement("article");

  const h3 = document.createElement("h3");
  article.appendChild(h3);
  h3.classList.add("major");
  h3.textContent = lista[indice].nome;

  const a = document.createElement("a");
  a.classList.add("special");
  article.appendChild(a);
  a.setAttribute("href", link);
  a.setAttribute("target", "_blank");
  a.textContent = data.owner.login;

  const span = document.createElement("span");
  article.appendChild(span);
  span.textContent = "⭐".repeat(stars);
  features.appendChild(article);
}

for (i = 0; i < lista.length; i++) {
  fetch(`https://api.github.com/repos/${lista[i].username}/${lista[i].repo}`)
    .then((response) => response.json())
    .then((data) => showRepos(data, i));
}
